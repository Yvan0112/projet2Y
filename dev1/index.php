<h2>Qu'est-ce que le nouveau coronavirus?</h2>
        <p>Les coronavirus (surnommés CoV) sont une famille de virus plus ou moins sévères : selon le site du gouvernement, ils peuvent provoquer de simples rhumes ou des pathologies plus lourdes telles que le syndrome respiratoire du Moyen-Orient (MERS) et le syndrome respiratoire aigu sévère (SRAS). Aujourd’hui, on en connaît six espèces. Le nouveau coronavirus découvert en 2019 a été baptisé SARS-CoV-2 et la maladie qu’il entraîne, CoVid-19.</p>
        
