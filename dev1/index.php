<h2>Quel est le mode de transmission?</h2>
<p>La transmission interhumaine, c’est-à-dire de personne à personne, a rapidement été confirmée. La maladie se transmet par les voies respiratoires, par les postillons (éternuements, toux). Le gouvernement explique que l’“on considère qu’un contact étroit avec une personne malade est nécessaire pour transmettre la maladie : même lieu de vie, contact direct à moins d’un mètre lors d’une toux, d’un éternuement ou une discussion en l’absence de mesures de protection". Une récente étude chinoise publiée dans The Lancet a par ailleurs montré que la durée moyenne de l'excrétion virale, définie comme l'expulsion des particules virales du corps, était de 20 jours chez des personnes considérées comme guéries de l'infection à CoVid-19, c'est-à-dire ne présentant plus de symptômes. Chez les 54 personnes décédées étudiées, le virus était détectable du début de la maladie jusqu'à leur mort. Néanmoins, le directeur général de l’Organisation mondiale de la santé (OMS) Tedros Adhanom Ghebreyesus a déclaré le 3 mars 2020 qu'il est peu probable que les porteurs du virus asymptomatiques soient très contagieux : "Avec la grippe, les personnes infectées mais asymptomatiques sont des moteurs de transmission importants, ce qui ne semble pas être le cas avec le CoVid-19. Des données chinoises montrent que seulement 1 % des cas rapportés sont asymptomatiques, et la plupart des cas développent des symptômes dans les deux jours suivant l’infection". Aucun autre mode de transmission n’a à ce jour été identifié. Il est donc peu probable que la maladie se transmette par l’eau, ou encore par les colis en provenance de Chine, comme le craignaient certaines personnes.</p>
